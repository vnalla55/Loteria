                           <h3>Lotería Dominicana Online realiza tres tipos de juegos</h3>

<div class="gap gap-mini"></div>
<h4 class="text-primary"><b>Quiniela</b></h4>
Debes elegir <b>1 número</b> del 1 al 100 y tienes tres oportunidades de ganar, es decir, que puedes ganar con cualquiera de los tres números que resulten ganadores en el sorteo.
<div class="gap gap-mini"></div>
<b>Por cada RD$1.00 apostado gana:</b>
<div class="gap gap-mini"></div>
	El primer premio gana: 	<b> RD$60.00</b>
<div class="gap gap-mini"></div>
	El segundo premio gana: 	<b> RD$10.00</b>
<div class="gap gap-mini"></div>
	El tercer premio gana: 	<b> RD$5.00</b>
<div class="gap gap-mini"></div>
<h4 class="text-primary"><b>Palé</b></h4>
Debes elegir <b>2 números</b> del 1 al 100 y tienes tres oportunidades de ganar, o sea, que puedes ganar combinando dos números cualquiera de los tres números que resulten ganadores en el sorteo sin importar el orden que elijas los números.
<div class="gap gap-mini"></div>
<b>Por cada RD$1.00 apostado las siguientes combinaciones ganan:</b>
<div class="gap gap-mini"></div>
	El primer premio y segundo premio gana: 	<b> RD$1000.00</b>
<div class="gap gap-mini"></div>
	El primer premio y tercer premio gana: 	<b> RD$1000.00</b>
<div class="gap gap-mini"></div>
	El segundo premio y tercer premio gana: 	<b> RD$100.00</b>
<div class="gap gap-mini"></div>
<h4 class="text-primary"><b>Tripleta</b></h4>
Debes elegir <b>3 números</b> del 1 al 100 y tienes dos formas de ganar, la primera es si acierta los tres números que resulten ganadores en el sorteo sin importar el orden que halla elegidos estos, y la segunda es si acierta dos números cualquiera de los tres números que resulten ganadores en el sorteo sin importar el orden de la selección.
<div class="gap gap-mini"></div>
<b>Por cada RD$1.00 apostado las siguientes combinaciones gana:</b>
<div class="gap gap-mini"></div>
	Si acierta los tres premios gana: 	<b> RD$20.000.00</b>
<div class="gap gap-mini"></div>
	Si acierta dos de los tres premios gana: 	<b> RD$100.00</b>
<div class="gap gap-mini"></div>
<h4 class="text-primary"><b>Formas como se realiza el sorteo</b></h4>
Se introducen 100 bolos numerados del 1 al 100, en cada una de las tres tómbolas; luego se extrae un número de cada una de las tómbolas, de los cuales el primer número extraído de la primera tómbola corresponde al <b>PRIMER PREMIO</b>, el segundo número extraído de la segunda tómbola corresponde al <b>SEGUNDO PREMIO</b> y el tercer numero extraído de la tercera tómbola será el <b>TERCER PREMIO</b>. El sorteo es oficialmente realizado por la Loteria Nacional De La Republica Dominicana.
<div class="gap gap-mini"></div>
<h4 class="text-primary"><b>Como jugar Quiniela</b></h4>
En la pagina principal, hay un tablero numerado del 1 al 100, encima de dicho tablero están opciones de <b>Quiniela, Palé, Tripleta, y Lucky Picker</b>. Para jugar "Quiniela" solo tiene que hacer click en botón Quiniela; luego hace click en el <b>número</b> dentro del tablero que desea jugar, después mas abajo está la opción Apuesta con las diferentes cantidades en EU$, solo tiene que hacer un click en la cantidad que desee apostar. Finalmente hace click en la opción <mark>Aceptar</mark>, su jugada será transferida a Su Ticket al lado derecho del tablero; y así puedes jugar sucesivamente cuantos juegos desee. Cuando este listo para ejecutar su apuesta completa, simplemente le hace click al botón de <mark>Confirmar/Aceptar</mark> dentro de Su Ticket y su Balance será ajustado de acuerdo a su apuesta.
<div class="gap gap-mini"></div>
<h4 class="text-primary"><b>Como jugar Palé</b></h4>
En la pagina principal, hay un tablero numerado del 1 al 100, encima de dicho tablero están las opciones <b>Quiniela, Palé, Tripleta, y Lucky Picker</b>; Para jugar "Palé" solo tiene que hacer click en botón Palé; luego hace click en dos <b>(2) números</b> dentro del tablero que desea jugar, después mas abajo está la opción Apuesta con las diferentes cantidades en RD$, solo tiene que hacer click en la cantidad que desee apostar. Finalmente hace click en la opción <mark>Aceptar</mark>, su jugada cera transferida a Su Ticket al lado derecho del tablero; y así Puedes jugar sucesivamente cuantos juegos desee. Cuando este listo para ejecutar su apuesta completa, simplemente le hace click al botón de <mark>Confirmar/Aceptar</mark> dentro de Su Ticket y su Balance será ajustado de acuerdo a su apuesta.
<div class="gap gap-mini"></div>
<h4 class="text-primary"><b>Como jugar Tripleta</b></h4>
En la pagina principal, hay un tablero numerado del 1 al 100, encima de dicho tablero están las opciones <b>Quiniela, Palé, Tripleta, y Lucky Picker</b>; Para jugar "Tripleta" solo tiene que hacer click en el botón Tripleta; luego hace click en tres <b>(3) números</b> dentro del tablero que desea jugar, después mas abajo está la opción Apuesta con las diferentes cantidades en RD$, solo tiene que hacer click en la cantidad que desee apostar. Finalmente haga click en la opción <mark>Aceptar</mark>, su jugada será transferida a Su Ticket al lado derecho del tablero; y así puedes jugar sucesivamente cuantos juegos desee. Cuando este listo para ejecutar su apuesta completa, simplemente le hace click al botón de <mark>Confirmar/Aceptar</mark> dentro de Su Ticket y su Balance será ajustado de acuerdo a su apuesta.
