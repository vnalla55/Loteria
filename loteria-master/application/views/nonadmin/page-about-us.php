                           <h3>¿Que es Tu Banquita?</h3>

<p>Tu Banquita es el portal que reúne a las más importantes Casas de Apuestas. Todo jugador tiene en sus manos su Casa de Apuesta Favorita por medio de su teléfono,  celular o vía el portal. Las 24 horas del día, 7 días a la semana.

No necesita dejar de hacer lo que está haciendo, al despertarse de un sueño revelador, durante una fiesta familiar, en la Playa o simplemente pasando un buen rato con sus amistades con el simple acceso a una computadora con servicio de internet o su móvil podrá jugar sus números favoritos como – Quinielas, Pales y Tripletas – en la Casa de Apuestas de su preferencia y confianza.</p>
<br>
<h3>¿Como se crea un Usuario?</h3>
<p>En el portal eliges Registrame y entras tu información general (nombre de usuario, contraseña), banca de apuesta de tu preferencia (aunque para cada jugada puedes elegir donde realizar tu jugada)</p>
<br>
<h3>¿Como se realiza una Jugada En Línea?</h3>
<p>Sus selecciones se pueden hacer a través del portal web . Primero elija su preferencia banca, luego de seleccionar las obras que desea que generaste en el billete. Una vez que esté listo a la comprobación elegir el método de pago (tarjeta de crédito , tarjeta de prepago o deposito ) y luego imprimir su billete.</p>
<br>
<h3>¿Como se realiza una jugada por Teléfono?</h3>
<p>El jugador marca el numero 1-200 y un agente le atenderá su llamada. El Agente le preguntara por su Casa de Apuestas favorita; le tomara su jugada; luego de confirmar la jugada eliges la forma de pago (tarjeta de crédito, tarjeta pre-pago, deposito en cuenta bancaria o debitando de los fondos disponibles) y el agente le proveerá el código único de su ticket y le enviará el mismo vía correo electrónico.</p>
<br>
<h3>¿Como se cobra una jugada ganadora?</h3>
<p>Luego de que usted resulte ganador, puede confirmar su ticket ganador en la página web o llamando a nuestro número 1-200. Su premio puede ser depositado en una cuenta establecida por el jugador, agregada en su balance o las sucursales establecidas por el Consorcio de apuestas responsable por su jugada.</p>
